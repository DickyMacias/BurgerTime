
package Modelo;


public class Producto {
    String pan;
    String pollo;
    String vaca;
    String onion;
    String condimentos;
    
    public Producto(){
        pan="";
        pollo="";
        vaca="";
        onion="";
        condimentos="";
    }

  public String getPan() {
    return pan;
  }

  public void setPan(String pan) {
    this.pan = pan;
  }

  public String getPollo() {
    return pollo;
  }

  public void setPollo(String pollo) {
    this.pollo = pollo;
  }

  public String getVaca() {
    return vaca;
  }

  public void setVaca(String vaca) {
    this.vaca = vaca;
  }

  public String getOnion() {
    return onion;
  }

  public void setOnion(String onion) {
    this.onion = onion;
  }

  public String getCondimentos() {
    return condimentos;
  }

  public void setCondimentos(String condimentos) {
    this.condimentos = condimentos;
  }
    

}
